@router.post("/{import_id}/rollback")
async def rollback_import_api(
    import_id: str,
    admin: dict = Depends(current_team_or_admin)
):
    """Rollback an import, restoring modified talents to their exact previous state and removing new records."""
    try:
        total_restored = await rollback_import_records(import_id)
        
        await db.import_history.update_one(
            {"id": import_id},
            {"$set": {"status": "rolled_back", "rolled_back_at": datetime.now(timezone.utc).isoformat()}}
        )
        
        return {
            "success": True,
            "deleted_count": total_restored,
            "message": f"Successfully rolled back {total_restored} talent records to their pre-import state."
        }
    except Exception as e:
        logger.error(f"[Imports] Rollback failed for import_id={import_id}: {e}", exc_info=True)
        raise HTTPException(500, f"Rollback failed: {str(e)}")
