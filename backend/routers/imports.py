@router.post("/upload")
async def upload_import_file(
    file: UploadFile = File(...),
    admin: dict = Depends(current_team_or_admin)
):
    """Upload a CSV or XLSX sheet and extract headers, metadata, checksum hash, and preview data."""
    try:
        content = await file.read()
        filename = file.filename or "import.csv"
        size = len(content)
        
        if size > 10 * 1024 * 1024:
            raise HTTPException(400, "File size exceeds maximum limit of 10MB")
            
        checksum = compute_file_checksum(content)
        
        # Check if checksum was already imported
        existing_history = await db.import_history.find_one({"file_checksum": checksum})
        is_duplicate_file = existing_history is not None
        duplicate_filename = existing_history.get("filename") if is_duplicate_file else None
        
        headers, raw_rows = parse_file(content, filename)
        detected_mapping = auto_detect_headers(headers)
        sample_rows = raw_rows[:10]
        
        return {
            "filename": filename,
            "size": size,
            "row_count": len(raw_rows),
            "file_checksum": checksum,
            "is_duplicate_file": is_duplicate_file,
            "duplicate_filename": duplicate_filename,
            "headers": headers,
            "detected_mapping": detected_mapping,
            "raw_rows": raw_rows,
            "sample_rows": sample_rows
        }
    except Exception as e:
        logger.error(f"[Imports] Upload failed: {e}", exc_info=True)
        raise HTTPException(400, f"Failed to parse file: {str(e)}")

@router.post("/preview")
async def preview_transforms_api(
    req: PreviewRequest,
    admin: dict = Depends(current_team_or_admin)
):
    """Dry-run transformations on sample rows, presenting the exact constructed MongoDB document JSON."""
    preview = []
    for idx, row in enumerate(req.raw_rows[:100]):
        transformed = apply_transforms(row, req.field_mapping)
        
        # Build exact Mongo document shape representation
        doc_preview = {
            "id": f"autogenerated-uuid-placeholder",
            **transformed,
            "import_id": "session-uuid-placeholder",
            "created_at": datetime.now(timezone.utc).isoformat(),
            "updated_at": datetime.now(timezone.utc).isoformat(),
            "status": "SUBMITTED",
            "approval_status": "approved",
            "created_by": admin["id"],
            "media": transformed.get("media", []),
            "availability": "available"
        }
        
        preview.append({
            "row_number": idx + 2,
            "original": {k: str(v) for k, v in row.items() if v is not None},
            "transformed": transformed,
            "mongo_document": doc_preview
        })
    return preview

