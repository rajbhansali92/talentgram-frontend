@router.post("/import")
async def execute_import_api(
    req: ImportExecuteRequest,
    admin: dict = Depends(current_team_or_admin)
):
    """Enqueues the import job to the persistent MongoDB task queue. Enforces a Global Ingestion Lock."""
    # Enforce Global Ingestion Lock: check if there is an active import running
    active_job = await db.import_sessions.find_one({"status": {"$in": ["queued", "processing"]}})
    if active_job:
        raise HTTPException(
            400, 
            f"Global Ingestion Lock active: Another import pipeline ({active_job.get('filename')}) is currently being processed by the worker queue."
        )

    session_id = await create_import_session(
        filename=req.filename,
        total_rows=len(req.records),
        records=req.records,
        dup_actions=req.dup_actions,
        admin_id=admin["id"],
        admin_email=admin.get("email")
    )
    
    # Save the file checksum on the session and history placeholder
    if req.file_checksum:
        await db.import_sessions.update_one({"_id": session_id}, {"$set": {"file_checksum": req.file_checksum}})

    return {
        "import_id": session_id,
        "status": "queued",
        "message": "Import job successfully queued in persistent worker database."
    }

